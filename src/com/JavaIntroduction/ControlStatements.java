public class ControlStatements {

	public static void main(String[] args) {
		int a=10,b=30,c=20;
		if(a>b && a>c)
		{
			System.out.println("A is larger number");
		}
		else if(b>c)
		{
			System.out.println("B is larger number");
		}
		else
		{
			System.out.println("C is larger numbers");
		}

	}

}
